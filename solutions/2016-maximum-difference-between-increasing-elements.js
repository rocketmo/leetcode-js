const assert = require('assert');

/**
 * First solution: Two loops
 * @param {number[]} nums
 * @return {number}
 */
var maximumDifference = function(nums) {
  let ans = -1;

  for (let i = 0; i < nums.length - 1; i += 1) {
    const num1 = nums[i];
    for (let j = i + 1; j < nums.length; j += 1) {
      const num2 = nums[j];
      if (num2 > num1) {
        ans = Math.max(ans, num2 - num1);
      }
    }
  }

  return ans;
};

/**
 * Second solution: One loop
 * @param {number[]} nums
 * @return {number}
 */
var maximumDifference = function(nums) {
  let ans = -1;
  let min = nums[0];
  let max = null;

  for (let i = 1; i < nums.length; i += 1) {
    const num = nums[i];

    if (num <= min) {
      min = num;
      max = null;
    } else if (max === null || num > max) {
      max = num;
      ans = Math.max(ans, max - min);
    }
  }

  return ans;
};

